/* Local function */
int calcDacVolt(float Votage_start, float Votage_end, uint32_t *dac_volt)
{
	float mV_per_ele;
	float deltaV;
	int numDacVoltage;
	float step;
	float dac_start, dac_end;
	int ii;

	deltaV = MAX_VOLTAGE - MIN_VOLTAGE;
	mV_per_ele = deltaV / ((float)(1 << BIT_WIDTH));

	dac_start = Votage_start / mV_per_ele + (1 << (BIT_WIDTH - 1));
	dac_end = Votage_end / mV_per_ele + (1 << (BIT_WIDTH - 1));
	numDacVoltage = (int)round(TIME / TIME_SAMP);

	step = (dac_end - dac_start) / (float)(numDacVoltage - 1);

	for (ii = 0; ii < numDacVoltage; ii++)
	{
		dac_volt[ii] = round(dac_start + ii * step);
	}

	return numDacVoltage;
}

